#include <stdio.h>
#include <string.h>
#include "FuntionsCalculator.h"

int sum (int num1, int num2)
{
    return num1 + num2;
}

int subtraction(int num1, int num2)
{
    return num1 - num2;
}

float division(float num1, float num2)
{
    return num1 / num2;
}

int multiplication(int num1, int num2)
{
    return num1 * num2;
}
